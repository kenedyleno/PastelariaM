    private void preencherTabela(Produto produto) {
        DefaultTableModel modelo = (DefaultTableModel) cp_tabela2.getModel();
        Object[] linha = new Object[5];
        modelo.setRowCount(0);
        linha[0] = produto.getCodigo();
        linha[1] = produto.getDescricao();
        linha[2] = produto.getValor();

        modelo.addRow(linha);

        cp_tabela2.setModel(modelo);
    }

    private void preencherTabela(List<Produto> lista) {

        DefaultTableModel modelo = (DefaultTableModel) cp_tabela.getModel();
        Object[] linha = new Object[5];
        modelo.setRowCount(0);

        for (Produto produto : lista) {
            linha[0] = produto.getCodigo();
            linha[1] = produto.getDescricao();
            linha[2] = produto.getValor();

            modelo.addRow(linha);
        }
        cp_tabela.setModel(modelo);

    }

    private void preencherNota(List<Produto> lista) {

        DefaultTableModel modelo = (DefaultTableModel) CP_NOTA.getModel();
        Object[] linha = new Object[5];
        modelo.setRowCount(0);

        for (Produto produto : lista) {
            linha[0] = produto.getDescricao();
            linha[1] = produto.getQuantidade();
            linha[2] = produto.getValorVenda();

            modelo.addRow(linha);
        }
        CP_NOTA.setModel(modelo);

    }

