    private void CP_CODIGOKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CP_CODIGOKeyPressed
        // TODO add your handling code here:

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            String cod = CP_CODIGO.getText();
            int qtd = Integer.parseInt(CP_QUANT.getText());
            Produto p = controleProduto.recuperar(cod);
            double valor = p.getValor() * qtd;
            p.setValorVenda(valor);
            p.setQuantidade(qtd);
            CP_R.setText(controleProduto.recuperarValor(cod));

            valorTotal = valorTotal + valor;
            CP_TOTAL.setText("TOTAL = " + valorTotal);
            listaVenda.add(p);
            preencherNota(listaVenda);

        }
    }//GEN-LAST:event_CP_CODIGOKeyPressed

