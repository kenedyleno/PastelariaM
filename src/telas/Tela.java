    private void BT_FIMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_FIMActionPerformed
        try {
            // TODO add your handling code here:
            Historico venda = new Historico();
            venda.setData(formatador.parse(formatador.format(new Date())));
            venda.setValortotal(valorTotal);
            venda.setProduto(listaVenda);

            String msg = controleHistorico.salvar(venda);
            JOptionPane.showMessageDialog(this, msg + "\nTotal = " + valorTotal);

            valorTotal = 0;
            listaVenda.clear();
            CP_CODIGO.setText("");
            CP_QUANT.setText("");
            DefaultTableModel modelo = (DefaultTableModel) CP_NOTA.getModel();
            modelo.setRowCount(0);
        } catch (ParseException ex) {
            Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BT_FIMActionPerformed

